// ============================================
// Binary search로 구간 탐색
// ============================================
function findTaxRow(
  monthlyTaxable: number,
): { min: number; max: number; taxes: readonly number[] } | null {
  let lo = 0;
  let hi = TAX_TABLE.length - 1;
  while (lo <= hi) {
    const mid = (lo + hi) >>> 1;
    const row = TAX_TABLE[mid];
    if (monthlyTaxable < row.min) {
      hi = mid - 1;
    } else if (monthlyTaxable >= row.max) {
      lo = mid + 1;
    } else {
      return row;
    }
  }
  return null;
}

// ============================================
// 10,000천원 초과 구간 공식 계산
// ============================================
function calcHighIncomeTax(
  monthlyTaxable: number,
  dependentIndex: number,
): number {
  // 10,000천원 구간(9,980~10,000)의 세액을 기준값으로 사용
  const lastRow = TAX_TABLE[TAX_TABLE.length - 1];
  const baseTax = lastRow.taxes[dependentIndex];

  const excess = monthlyTaxable - 10_000_000; // 10,000천원 초과액 (원)

  if (monthlyTaxable <= 14_000_000) {
    // 10,000~14,000천원: 기준세액 + 초과액 × 98% × 35%
    return baseTax + Math.floor(excess * 0.98 * 0.35);
  } else if (monthlyTaxable <= 28_000_000) {
    // 14,000~28,000천원: 기준세액 + 1,372,000 + (14,000천원 초과액) × 98% × 38%
    return (
      baseTax +
      1_372_000 +
      Math.floor((monthlyTaxable - 14_000_000) * 0.98 * 0.38)
    );
  } else if (monthlyTaxable <= 30_000_000) {
    // 28,000~30,000천원: 기준세액 + 6,585,600 + (28,000천원 초과액) × 98% × 40%
    return (
      baseTax +
      6_585_600 +
      Math.floor((monthlyTaxable - 28_000_000) * 0.98 * 0.4)
    );
  } else if (monthlyTaxable <= 45_000_000) {
    // 30,000~45,000천원: 기준세액 + 7,369,600 + (30,000천원 초과액) × 40%
    return (
      baseTax + 7_369_600 + Math.floor((monthlyTaxable - 30_000_000) * 0.4)
    );
  } else if (monthlyTaxable <= 87_000_000) {
    // 45,000~87,000천원: 기준세액 + 13,369,600 + (45,000천원 초과액) × 42%
    return (
      baseTax + 13_369_600 + Math.floor((monthlyTaxable - 45_000_000) * 0.42)
    );
  } else {
    // 87,000천원 초과: 기준세액 + 31,009,600 + (87,000천원 초과액) × 45%
    return (
      baseTax + 31_009_600 + Math.floor((monthlyTaxable - 87_000_000) * 0.45)
    );
  }
}

// ============================================
// 20세 이하 자녀 세액공제 (소득세법 제59조의2)
// ============================================
function calcChildTaxCredit(childrenUnder20: number): number {
  if (childrenUnder20 <= 0) return 0;
  if (childrenUnder20 === 1) return 12_500; // 월 12,500원
  if (childrenUnder20 === 2) return 29_160; // 월 29,160원
  // 3명 이상: 29,160 + (자녀수-2) × 25,000원
  return 29_160 + (childrenUnder20 - 2) * 25_000;
}

// ============================================
// 메인 계산 함수: 간이세액표 기반 소득세
// ============================================
/**
 * 근로소득 간이세액표 기반 소득세 계산
 *
 * @param monthlyTaxable 월 과세급여액 (비과세 제외, 원 단위)
 * @param dependents 공제대상 부양가족 수 (본인 포함, 기본값 1)
 * @param childrenUnder20 20세 이하 자녀 수 (기본값 0)
 * @returns 월 소득세액 (원 단위, 10원 미만 절사)
 */
export function calculateSimpleIncomeTax(
  monthlyTaxable: number,
  dependents: number = 1,
  childrenUnder20: number = 0,
): number {
  // 106만원 이하: 세액 0원
  if (monthlyTaxable <= 1_060_000) return 0;

  // 부양가족 수 범위 보정 (최소 1명 = 본인, 최대 11명)
  dependents = Math.max(1, Math.min(dependents, 11));
  childrenUnder20 = Math.max(0, childrenUnder20);

  const dependentIndex = dependents - 1; // 배열 인덱스 (0~10)

  let tax: number;

  if (monthlyTaxable >= 10_000_000) {
    // 10,000천원 초과: 공식 계산
    tax = calcHighIncomeTax(monthlyTaxable, dependentIndex);
  } else {
    // lookup table에서 구간 탐색
    const row = findTaxRow(monthlyTaxable);
    if (!row) return 0; // 테이블 범위 밖 (770천원 미만)
    tax = row.taxes[dependentIndex];
  }

  // 20세 이하 자녀 세액공제 차감
  const childCredit = calcChildTaxCredit(childrenUnder20);
  tax = Math.max(0, tax - childCredit);

  // 10원 미만 절사
  return Math.floor(tax / 10) * 10;
}

/**
 * 지방소득세 계산 (소득세의 10%)
 */
export function calculateLocalIncomeTax(incomeTax: number): number {
  return Math.floor((incomeTax * 0.1) / 10) * 10;
}
